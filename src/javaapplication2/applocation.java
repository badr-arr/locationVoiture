    private void searchLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchLabelMouseClicked
       if(SearchType.equals("Client")){
         ClientTab.setModel(DbUtils.resultSetToTableModel(trouverCinClient(searchField.getText())));
       }
       if(SearchType.equals("Tarif")){
          FactureTab.setModel(DbUtils.resultSetToTableModel(trouverFactureR(Integer.parseInt(searchField.getText()))));

       }
       
        if(SearchType.equals("Reservation")){
          ResaTab.setModel(DbUtils.resultSetToTableModel(trouveNumResaR(Integer.parseInt(searchField.getText()))));

       }
       
            
        searchField.setEnabled(false); 
        searchField.setText(null);
        ImageIcon in=new ImageIcon(getClass().getResource("search boxes1.png")); 
        searchLabel.setIcon(in);  
        
    }//GEN-LAST:event_searchLabelMouseClicked

